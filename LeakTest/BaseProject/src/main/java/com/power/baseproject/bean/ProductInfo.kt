package com.power.baseproject.bean

import java.io.Serializable

class ProductInfo:Serializable {
    var id: Int? = null
    var cid: Int? = null
    var pid: Int? = null
    var langcode: String? = null
    var name: String? = null
    var subject: String? = null
    var mpic: String? = null
    var fireware: String? = null
    var manifast: String? = null
    var mobipic: String? = null
    override fun toString(): String {
        return "ProductInfo(id=$id, cid=$cid, pid=$pid, langcode=$langcode, name=$name, subject=$subject, mpic=$mpic, fireware=$fireware, manifast=$manifast, mobipic=$mobipic)"
    }
}